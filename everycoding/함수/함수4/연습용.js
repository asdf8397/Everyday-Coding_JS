// 함수(function)의 예시1
function get_argument(arg) {
    return arg
};
console.log(get_argument(10));
console.log(get_argument(20));
console.log(get_argument(30));

function get_arg(arg1) {
    return arg1*3
};
console.log(get_arg(10));
console.log(get_arg(20));
console.log(get_arg(30));

function get_around(arg2, arg3) {
    return arg2 + arg3
};
console.log(get_around(2,3));
console.log(get_around(3,4));
console.log(get_around(10,20));

function get_func(arg4, arg5) {
    return arg4*10 + arg5*10
};

console.log(get_func(10,20));
console.log(get_func(20,30));