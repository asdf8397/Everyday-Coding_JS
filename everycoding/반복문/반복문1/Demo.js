// 반복문
/*
While 형식
while(조건) {
    반복해서 실행할 코드
}
*/

while(true) { // while(true)는 무한루프이다. ,,,, 실행하면 컴퓨터한테 무리를준다.
    document.write("Coding everybody<br>")
}

while(false) { // while(false)는 반복문이 무한루프가 실행되지않는다.
    document.write("Coding everybody<br>")
}

// JS에서 document.write는 명령어를 웹에 출력하기위해서 사용한다.
