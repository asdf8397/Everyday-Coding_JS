// API란?
/*
Application Programming Interface의 약자로 프로그램이 동작하는 환경을
제어하기 위해서 환경에서 제공되는 조작 장치이다.
이 조작 장치는 프로그래밍 언어를 통해서 조작할 수 있다
*/

// 레퍼런스와 튜토리얼
/*
프로그래밍을 공부하기 위한 자료는 크게 레퍼런스(reference)와 tutorial(안내서)
가 있다. 통상 튜토리얼은 언어의 문법을 설명하고, 레퍼런스는 명령어의 사전을
의미한다.
*/


